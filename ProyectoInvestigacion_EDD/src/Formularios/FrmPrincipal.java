    public void mostrar() {
        DefaultTableModel TablaVacunados = new DefaultTableModel();
        TablaVacunados.addColumn("Fecha - Primera Dosis");
        TablaVacunados.addColumn("Fecha - Segunda Dosis");
        TablaVacunados.addColumn("Fecha - Tercera Dosis");
        TablaVacunados.addColumn("Nombre");
        TablaVacunados.addColumn("Edad");
        TablaVacunados.addColumn("Sexo");
        TablaVacunados.addColumn("Tipo de Vacuna");
        TablaVacunados.addColumn("Dni");
        TablaVacunados.addColumn("Celular");
        TablaVacunados.addColumn("Correo Electrónico");
        TablaVacunados.addColumn("Ciudad");
        jtbBD.setModel(TablaVacunados);
        try {
            Statement leer = conect.createStatement();
            ResultSet rs = leer.executeQuery("select * from vacunados");
            String matriz[] = new String[11];
            while (rs.next()) {
                matriz[0] = rs.getString("Fecha_PD");
                matriz[1] = rs.getString("Fecha_SD");
                matriz[2] = rs.getString("Fecha_TD");
                matriz[3] = rs.getString("Nombre");
                matriz[4] = rs.getString("Edad");
                matriz[5] = rs.getString("Sexo");
                matriz[6] = rs.getString("Codigo_Vacuna");
                matriz[7] = rs.getString("Dni");
                matriz[8] = rs.getString("Celular");
                matriz[9] = rs.getString("Correo_Electrónico");
                matriz[10] = rs.getString("Ciudad");
                TablaVacunados.addRow(matriz);
            }
            jtbBD.setModel(TablaVacunados);
        } catch (Exception e) {
            lblInformacion.setText("Error papi");
        }
    }

