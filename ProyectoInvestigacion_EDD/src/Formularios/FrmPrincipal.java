    private void btnBuscarBusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarBusquedaActionPerformed
        // Declaración de Variables para separar la fecha
        String fechaPD, fechaSD, fechaTD, diaPD, diaSD, diaTD, mesPD, mesSD, mesTD, anioSD, aniosPD,aniosTD;
        // Evento de búsqueda en la base de datos.
        try {
            PreparedStatement pst = conect.prepareStatement("select * from vacunados where Dni = ?");
            // enviar a la base de datos el valor a buscar
            pst.setString(1, txtDniBuscarBusqueda.getText().trim());
            // clase que permite saber si encontró el valor
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                fechaPD = rs.getString("Fecha_PD");
                fechaSD = rs.getString("Fecha_SD");
                fechaTD = rs.getString("Fecha_TD");

                diaPD = fechaPD.split("/")[0];
                mesPD = fechaPD.split("/")[1];
                aniosPD = fechaPD.split("/")[2];
                diaSD = fechaSD.split("/")[0];
                mesSD = fechaSD.split("/")[1];
                anioSD = fechaSD.split("/")[2];
                diaTD = fechaTD.split("/")[0];
                mesTD = fechaTD.split("/")[1];
                aniosTD = fechaTD.split("/")[2];
                cbxDiaPD.setSelectedItem(diaPD);
                cbxDiaSD.setSelectedItem(diaSD);
                cbxDiaTD.setSelectedItem(diaTD);
                cbxMesPD.setSelectedItem(mesPD);
                cbxMesSD.setSelectedItem(mesSD);
                cbxMesTD.setSelectedItem(mesTD);
                cbxAnioPD.setSelectedItem(aniosPD);
                cbxAnioSD.setSelectedItem(anioSD);
                cbxAnioTD.setSelectedItem(aniosTD);
                txtNombresInicio.setText(rs.getString("Nombre"));
                txtEdadInicio.setText(rs.getString("Edad"));
                cbxSexoInicio.setSelectedItem(rs.getString("Sexo"));
                txtTipoVacunaInicio.setText(rs.getString("Codigo_Vacuna"));
                txtDniInicio.setText(rs.getString("Dni"));
                txtCelularInicio.setText(rs.getString("Celular"));
                txtCorreoInicio.setText(rs.getString("Correo_Electrónico"));
                txtCiudadInicio.setText(rs.getString("Ciudad"));
                lblInformacion.setText("Consulta Exitosa");
            } else {
                lblInformacion.setText("Participante no encontrado");
            }
        } catch (Exception e) {
            lblInformacion.setText("Error: " + e);
        }
    }//GEN-LAST:event_btnBuscarBusquedaActionPerformed

